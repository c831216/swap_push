#include "libft.h"

int	ft_toupper(int c)
{
	unsigned int	nc;
	int				dif;

	dif = 'A' - 'a';
	nc = (unsigned int)c;
	if (c >= 'a' && c <= 'z')
		nc = nc + dif;
	return (nc);
}
